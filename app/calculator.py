def calculate_roi(net_profit, total_investment):
    return (net_profit / total_investment) * 100

def calculate_cash_on_cash_return(annual_pre_tax_cash_flow, total_cash_invested):
    return (annual_pre_tax_cash_flow / total_cash_invested) * 100

def calculate_cap_rate(net_operating_income, current_market_value):
    return (net_operating_income / current_market_value) * 100